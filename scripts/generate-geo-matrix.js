const EARTH_RADIUS_KM = 6371;
const DECAY_KM = 400;
const MULTIPLIER_MAX = 3;
const MULTIPLIER_MIN = 1;
const PRECISION = 4;

function toRad(deg) {
    return (deg * Math.PI) / 180;
}

function haversineKm(a, b) {
    const dLat = toRad(b.lat - a.lat);
    const dLon = toRad(b.lon - a.lon);
    const lat1 = toRad(a.lat);
    const lat2 = toRad(b.lat);
    const h = Math.sin(dLat / 2) ** 2 + Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLon / 2) ** 2;
    return 2 * EARTH_RADIUS_KM * Math.asin(Math.sqrt(h));
}

function weightForDistance(distanceKm) {
    const weight = MULTIPLIER_MIN + (MULTIPLIER_MAX - MULTIPLIER_MIN) * Math.exp(-distanceKm / DECAY_KM);
    return Number(weight.toFixed(PRECISION));
}

function buildMatrix() {
    return HOSPITALS.map(from => HOSPITALS.map(to => weightForDistance(haversineKm(from, to))));
}

function validateMatrix(matrix) {
    if (matrix.length !== HOSPITALS.length) {
        throw new Error('Matrix size does not match hospitals list.');
    }
    matrix.forEach((row, i) => {
        if (row.length !== HOSPITALS.length) {
            throw new Error(`Matrix row ${i} is not the expected length.`);
        }
    });
}

function writeOutput(matrix, outputPath) {
    const body = JSON.stringify(matrix, null, 4);
    const output = `// Auto-generated by scripts/generate-geo-matrix.js\n// Weights use: 1 + 2 * exp(-distance_km / 400)\nconst GEO_WEIGHT_MATRIX = ${body};\n`;
    fs.writeFileSync(outputPath, output, 'utf8');
}

function main() {
    const outputPath = path.resolve(__dirname, 'geo-weight-matrix.js');
    const matrix = buildMatrix();
    validateMatrix(matrix);
    writeOutput(matrix, outputPath);
    console.log(`Wrote ${outputPath}`);
}

main();
